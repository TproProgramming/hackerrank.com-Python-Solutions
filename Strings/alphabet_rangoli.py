def print_rangoli(size):
    holder = '-'
    alphabet = 'abcdefghijklmnopqrstuvwxyz'
    
    lines = []  
    
    # Build the top half of the rangoli
    for i in range(size-1, -1, -1):
        left = holder.join(alphabet[size-1:i:-1] + alphabet[i:size])
        line = left.center(size*4-3, holder)
        lines.append(line)
    
    # Build the bottom half of the rangoli (excluding the middle line)
    for line in lines[:-1][::-1]:
        lines.append(line)
    
    for line in lines:
        print(line)

if __name__ == '__main__':
    n = int(input())
    print_rangoli(n)
